package com.oldwang.oldwangcrm.model;

/**
 * 房东实体类
 * 
 * @author Administrator
 * 
 */
public class Landlord {
	/**
	 * 房东编号id
	 */
	private int id;
	/**
	 * 姓名
	 */
	private String name;
	/**
	 * 联系方式
	 */
	private String phone;

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the phone
	 */
	public String getPhone() {
		return phone;
	}

	/**
	 * @param phone
	 *            the phone to set
	 */
	public void setPhone(String phone) {
		this.phone = phone;
	}

}
